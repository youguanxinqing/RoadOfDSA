/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func swapPairs(head *ListNode) *ListNode {
	if head == nil || head.Next == nil {
		return head
	}

	root := head.Next
	kids := swapPairs(head.Next.Next)
	head.Next.Next = head
	head.Next = kids
	return root
}
